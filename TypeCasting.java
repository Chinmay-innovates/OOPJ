public class TypeCasting {
    public static void main(String[] args) {
        double d = 10.04;
        long l = (long) d;
        int i = (int) l;
        System.out.println("d value is " + d);   
        System.out.println("l value is " + l);   
        System.out.println("i value is " + i);   
    }
}
